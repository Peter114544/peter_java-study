public class IncrementAndDecrementOperator {
    public static void main(String[] args) {
        int a = 10;//++/--
        a++;
        System.out.println(a);//11
        a--;
        System.out.println(a);//10
        ++a;
        System.out.println(a);//11
        --a;
        System.out.println(a);//10


    }
}
